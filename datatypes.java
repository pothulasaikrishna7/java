class datatypes{
    byte b=10; 
    int i=20;
    float f=20.5F; 
    char c='s';
    
    public static void main(String[] args){
       
        datatypes obj = new datatypes(); 
        System.out.println("byte=" + obj.b); 
        System.out.println("int=" + obj.i); 
        System.out.println("float=" + obj.f); 
        System.out.println("char=" + obj.c); 
    }
}